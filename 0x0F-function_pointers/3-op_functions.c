/**
 * op_add - adds @a and @b
 *
 * @a: first input
 * @b: second input
 *
 * Return: sum of @a and @b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtraction of @a by @b
 *
 * @a: first input
 * @b: second input
 *
 * Return: difference of @a and @b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - product of @a and @b
 *
 * @a: first input
 * @b: second input
 *
 * Return: product of @a and @b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides @a by @b
 *
 * @a: first input
 * @b: second input
 *
 * Return: results of division
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - reminder of a division
 *
 * @a: first input
 * @b: second input
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
